package homework2;

public class Calculator {
	public int Sum(int num1, int num2) {
		return num1 + num2;
	}
	public int Subtraction(int num1, int num2) {
		return num1 - num2;
	}
	public int Multiplication(int num1, int num2) {
		return num1 * num2;
	}
	public double Division(int num1, int num2) {
		return num1 / num2;
	}
}
